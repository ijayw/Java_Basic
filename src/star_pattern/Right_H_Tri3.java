package star_pattern;

public class Right_H_Tri3 {
public static void main(String[] args) {
       for(int i=1; i<=4 ;i++)
			{
				for(int j=1; j<=4 ;j++)
				{
					if(j>=5-i && j<=4)
					{
						System.out.print("*");
					}
					System.out.print("");
				}
			
				System.out.println();
			}
      
		}
		}

	
	


